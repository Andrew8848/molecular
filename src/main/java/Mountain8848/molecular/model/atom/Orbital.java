package Mountain8848.molecular.model.atom;

public class Orbital
{
   private boolean spin1;
   private boolean spin2;
   
   public boolean isSpin1()
   {
      return spin1;
   }
   
   public void setSpin1(boolean spin1)
   {
      this.spin1 = spin1;
   }
   
   public boolean isSpin2()
   {
      return spin2;
   }
   
   public void setSpin2(boolean spin2)
   {
      this.spin2 = spin2;
   }
}
